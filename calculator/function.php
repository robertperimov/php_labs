<?php

/**
 * @param int $num1
 * @param int $num2
 * @return int
 */
function sum($num1, $num2)
{
    return $num1 + $num2;
}

/**
 * @param int $num1
 * @param int $num2
 * @return int
 */
function minus($num1, $num2)
{
    return $num1 - $num2;
}

/**
 * @param int $num1
 * @param int $num2
 * @return int
 */
function multiply($num1, $num2)
{
    return $num1 * $num2;
}

/**
 * @param int $num1
 * @param int $num2
 * @return bool
 */
function compare($num1, $num2)
{
    return $num1 == $num2;
}